    private void SignUpbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpbtnActionPerformed
        // TODO add your handling code here:
        if (txtUsername.getText().equals("") || txtPassword.getPassword().length == 0
                || cmbRole.getSelectedItem().toString().equals("Select Role")) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields and select a role!");
            return;
        }

        try {
            Connection conClass = ConnectionClass.createConnection();

            // Check if username already exists
            String checkQuery = "SELECT * FROM login WHERE Username = ?";
            PreparedStatement checkStmt = conClass.prepareStatement(checkQuery);
            checkStmt.setString(1, txtUsername.getText());
            ResultSet checkResult = checkStmt.executeQuery();

            if (checkResult.next()) {
                JOptionPane.showMessageDialog(this, "Username already exists! Please choose another one.");
                checkResult.close();
                checkStmt.close();
                conClass.close();
                return;
            }

            // Insert new user with role
            String query = "INSERT INTO login (Username, Password, role) VALUES (?, ?, ?)";
            PreparedStatement pst = conClass.prepareStatement(query);
            pst.setString(1, txtUsername.getText());
            pst.setString(2, String.valueOf(txtPassword.getPassword()));
            pst.setString(3, cmbRole.getSelectedItem().toString().toLowerCase()); // 'staff' or 'customer'

            int rowsInserted = pst.executeUpdate();

            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "ðŸŽ‰ Registration Successful! You can now log in.");

                // Open login page after signup
                Login_UI loginForm = new Login_UI(); 
                loginForm.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Registration failed! Please try again.");
            }

            pst.close();
            conClass.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_SignUpbtnActionPerformed

